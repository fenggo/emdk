subroutine write_db
    use mode,only:npart,x,types,a,b,c
    implicit none
 
    integer            i

    open(37,file='coord.db',status='unknown')

    write(37,'(A10,A5)')'subroutine', ' XXXX'
    write(37,'(4X,A62)')'use mode,only:npart,ctype,x,types,mass,a0,b0,c0,cpart,sc,nelem'
    write(37,'(4X,A13)')'implicit none'
    write(37,*)

    write(37,'(4X,A35,I6)')'!Number of particles in unit cell= ',npart
    write(37,'(4X,A8,I6)')'cpart = ',npart
    write(37,'(4X,A9)')'nelem = 4'
    write(37,*)
    write(37,'(4X,A33)') "ctype(1:4) = (/'C','H','O','N' /) "
    write(37,'(4X,A45)') 'mass(1:4) = (/12.000, 1.008, 15.999, 14.000/)'
    write(37,*)
    !npart=cpart*sc(1)*sc(2)*sc(3)
    !call memory

    write(37,'(4X,A5,F10.6,A1,F10.6,A1,F10.6,A2)') 'a0=(/',a(1),',',a(2),',',a(3),'/)'
    write(37,'(4X,A5,F10.6,A1,F10.6,A1,F10.6,A2)') 'b0=(/',b(1),',',b(2),',',b(3),'/)'
    write(37,'(4X,A5,F10.6,A1,F10.6,A1,F10.6,A2)') 'c0=(/',c(1),',',c(2),',',c(3),'/)'
    write(37,*)
    write(37,'(4X,A29)')'npart=cpart*sc(1)*sc(2)*sc(3)'
    write(37,'(4X,A11)')'call memory'
    write(37,*)
    !write(37,'(4X,A8)') 'x = (/ &'
    do i=1,npart
       write(37,'(4X,A2,A2,I3,A3,A2,F10.6,A1,F10.6, A1,F10.6,A2)') 'x(',':,',i,')= ', '(/',x(1,i),',',x(2,i),',',x(3,i),'/)'
    enddo
    !write(37,'(7X,A8)') '/)'
    write(37,*)

    write(37,*)

    do i=1,npart
       write(37,'(7X,A6,I4,A2,I4)') 'types(',i,')=',types(i)
    enddo

    write(37,*)

    write(37,'(4X,A6)')'return'
    write(37,'(A3)')'end'

    close(37)
    return
end

